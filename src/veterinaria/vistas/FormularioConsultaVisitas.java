    private void btnBuscarTratamientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarTratamientoActionPerformed

        jTableMascota.setVisible(true);
        try {
            Mascota m = (Mascota) jcbMascota.getSelectedItem();
            if (m != null) {
                visitas = visitaData.obtenerVisitasxMascota(m.getIdMascota());
                visitasActivas = visitaData.obtenerVisitasxMascotaEActivo(m.getIdMascota());
                cargarTablaMascota();
                jrbVisitas.setEnabled(true);
                jrbVisitasActivas.setEnabled(true);
                if (visitas.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "La mascota " + m + " no tiene registrada ninguna visita");
                }
            }

        } catch (Exception e) {
            System.out.println("Error dentro de buscar tratamiento");
        }
    }//GEN-LAST:event_btnBuscarTratamientoActionPerformed

